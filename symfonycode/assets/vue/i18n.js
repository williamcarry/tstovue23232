/**
 * 翻译函数
 * @param {string} key - 翻译键
 * @param {string} defaultValue - 默认值
 * @param {string} locale - 语言代码，默认为 'zh-CN'
 * @returns {string}
 */
export function t(key, defaultValue = '', locale = 'zh-CN') {
  if (!messages[locale]) {
    locale = 'zh-CN'
  }
  return messages[locale][key] || defaultValue || key
}

/**
 * 获取所有翻译
 * @param {string} locale - 语言代码
 * @returns {object}
 */
export function getMessages(locale = 'zh-CN') {
  return messages[locale] || messages['zh-CN']
}

/**
 * 支持的语言列表
 */
export function getSupportedLocales() {
  return Object.keys(messages)
}

/**
 * 添加新的翻译
 * @param {string} locale - 语言代码
 * @param {object} newMessages - 新的翻译对象
 */
export function addMessages(locale, newMessages) {
  if (!messages[locale]) {
    messages[locale] = {}
  }
  Object.assign(messages[locale], newMessages)
}

export default {
  messages,
  t,
  getMessages,
  getSupportedLocales,
  addMessages
}
